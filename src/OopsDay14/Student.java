package OopsDay14;

public class Student {
	private String name;
	private String std;
	int mark;
	
	
	public void setname(String name) {
		this.name=name;
	}
	public String getname() {
		return name;
	}
	public void setstd(String std) {
		this.std=std;
	}
		public String getstd() {
			return std;
		}
		public void setmark(int mark) {
			this.mark=mark;
	}
		public int getmark() {
			return mark;
		}

}
